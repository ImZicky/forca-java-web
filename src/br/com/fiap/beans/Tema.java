package br.com.fiap.beans;

public class Tema {
		
	private String nomeTema;
	
	public Tema() {
		super();
	}

	public Tema(String nomeTema) {
		super();
		this.nomeTema = nomeTema;
	}

	public String getNomeTema() {
		return nomeTema;
	}

	public void setNomeTema(String nomeTema) {
		this.nomeTema = nomeTema;
	}
	
	
	
	
}
